a = [1,5,7,2,3,4,89,54,34,32]

#5번째 원소는 무엇인가, 출력결과 : 4
print(a[5])

#원소 5는 몇번째에 있는가, 출력결과 : 1
#일반적으로 숫자를 셀때 1,2,3.......이 아닌 컴퓨터는 0부터 센다/ 0,1,2,3,4,5......
print(a.index(5))

#리스트 전체 출력
print(a[:-1])

#마지막 원소 출력, 결과 : 32
print(a[-1])

#2번째부터 5번째 원소까지 출력
print(a[2:6])

#n은 몇번째에 있는가
b = "dasjksdsdnsakjdnsaj"
print(b.index("n"))
